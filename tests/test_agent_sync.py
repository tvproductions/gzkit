def _make_rule(
    rule_id: str = "test-rule",
    paths: list[str] | None = None,
    description: str = "Test rule",
    body: str = "# Test Rule\n\nRule content here.",
) -> CanonicalRule:
    """Create a CanonicalRule for testing."""
    return CanonicalRule(
        frontmatter=RuleFrontmatter(
            id=rule_id,
            paths=paths or ["**/*"],
            description=description,
        ),
        body=body,
        source_path=f".gzkit/rules/{rule_id}.md",
    )


def _write_canonical_rule(rules_dir: Path, rule_id: str, paths: list[str], body: str) -> None:
    """Write a canonical rule file to disk."""
    paths_yaml = "\n".join(f'  - "{p}"' for p in paths)
    content = f"---\nid: {rule_id}\npaths:\n{paths_yaml}\ndescription: Test rule\n---\n\n{body}\n"
    (rules_dir / f"{rule_id}.md").write_text(content, encoding="utf-8")


class TestRenderRuleForClaude(unittest.TestCase):
    """Tests for Claude rule rendering.

    @covers REQ-0.17.0-02-02
    """

    @covers("REQ-0.17.0-02-02")
    @covers("REQ-0.14.0-01-03")
    def test_global_rule_omits_frontmatter(self) -> None:
        """Global rules (paths: **/*) have no YAML frontmatter."""
        rule = _make_rule(paths=["**/*"], body="# Global Rule")
        rendered = render_rule_for_claude(rule)
        self.assertNotIn("---", rendered)
        self.assertIn("# Global Rule", rendered)
        self.assertIn("Generated by gz agent sync", rendered)

    @covers("REQ-0.14.0-02-02")
    @covers("REQ-0.17.0-02-02")
    def test_scoped_rule_has_paths_frontmatter(self) -> None:
        """Scoped rules get paths: YAML frontmatter."""
        rule = _make_rule(paths=["src/**/*.py", "tests/**"], body="# Scoped Rule")
        rendered = render_rule_for_claude(rule)
        self.assertIn("---", rendered)
        self.assertIn('  - "src/**/*.py"', rendered)
        self.assertIn('  - "tests/**"', rendered)
        self.assertIn("# Scoped Rule", rendered)
        self.assertIn("Generated by gz agent sync", rendered)


